import AWSPinpoint
import Foundation

/// Methods copied from `AWSPinpointTargetingClient`
protocol AWSPinpointTargetingClientBehavior {
    /**
     * Returns the current endpoint.
     * @return (PinpointEndpointProfile)
     */
    func currentEndpointProfile() -> PinpointEndpointProfile

    /**
     * Sends an update of the current endpoint
     */
    func updateEndpointProfile() async throws

    /**
     * Updates with the provided endpoint profile, AWSPinpointTargetingClient attributes and metrics are added to the
     *  profile.
     */
    func update(_ endpointProfile: PinpointEndpointProfile) async throws

    /**
     * Adds the specified attribute to the current endpoint profile generated by this client.
     * @param attributes A list of strings with the values of the attribute
     * @param key the name of the attribute to add
     */
    func addAttributes(_ attributes: [Any], forKey key: String)

    /**
     * Removes the specified attribute from the current endpoint profile generated by this client.
     * @param key the key of the attribute to remove
     */
    func removeAttributes(forKey key: String)

    /**
     * Adds the specified metric to the current endpoint profile generated by this client.
     * @param metric the value of the metric
     * @param key the name of the metric to add
     */
    func addMetric(_ metric: Float, forKey key: String)

    /**
     * Removes the specified metric from the current endpoint profile generated by this client.
     * @param key the key of the metric to remove
     */
    func removeMetric(forKey key: String)
}
